// ===== Вспомогательное =====

function pickRandomQuestions(source: Question[], count: number): Question[] {
  const shuffled = [...source].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

function isAnswerCorrect(question: Question, optionId: string): boolean {
  const option = question.options.find((o) => o.id === optionId);
  return Boolean(option && option.isCorrect);
}

const BASE_MAX_ERRORS_FRACTION = 0.2; // допускаем до 20% ошибок

type Mode = "idle" | "credit" | "all" | "errors" | "topics" | "topicQuiz";

type ActiveTopic =
  | {
      key: TopicKey;
      title: string;
    }
  | null;
// ===== Статистика тестов (localStorage) =====

type ModeForStats = "credit" | "all" | "errors" | "topic";

type QuizStats = {
  totalRuns: number;
  passedCount: number;
  failedCount: number;
  perMode: Record<
    ModeForStats,
    {
      runs: number;
      passed: number;
      failed: number;
    }
  >;
};

const STATS_KEY = "quizStats";

function loadStats(): QuizStats {
  try {
    const raw = localStorage.getItem(STATS_KEY);
    if (!raw) throw new Error("no stats");
    return JSON.parse(raw) as QuizStats;
  } catch {
    return {
      totalRuns: 0,
      passedCount: 0,
      failedCount: 0,
      perMode: {
        credit: { runs: 0, passed: 0, failed: 0 },
        all: { runs: 0, passed: 0, failed: 0 },
        errors: { runs: 0, passed: 0, failed: 0 },
        topic: { runs: 0, passed: 0, failed: 0 },
      },
    };
  }
}

function saveStats(stats: QuizStats) {
  localStorage.setItem(STATS_KEY, JSON.stringify(stats));
}

